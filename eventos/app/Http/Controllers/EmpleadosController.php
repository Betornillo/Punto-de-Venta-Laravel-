     */
    public function store(Request $request)
    {
        $empleados = new Empleados();
	$empleados->nombre = Input::get('nombre');
	$empleados->fechaing = Input::get('fechaing');
        $empleados->activo = (Input::get("activo") == "on" ) ? true : false;
	$empleados->edad = Input::get('edad');
	$empleados->fechana = Input::get('fechana');
	$empleados->save();
	return response()->json([
	"response" => "Guardado"
	],201);
    }

